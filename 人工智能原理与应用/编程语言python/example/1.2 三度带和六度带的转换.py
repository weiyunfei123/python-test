#3度带和六度带的转换

x=eval(input("请选择需要转换的带数3度或者6度,输入3或者6表示带号："))
du,fen,miao=eval(input("请输入任意经度，格式：度，分，秒："))
L=du+fen/60+miao/3600

if x==6:
    N=int((L+3)/6+0.5)
    L1=6*N-3
    print("6度带的带号为:{},中央子午线为:{}".format(N, L1))
if x==3:
    N=int(L/3+0.5)
    L1=3*N
    print("3度带的带号为:{},中央子午线为：{}".format(N,L1))
